void main() {
  // Integer data type
  int age = 30;
  print("Age: $age");

  // Double data type
  double height = 5.9;
  print("Height: $height");

  // String data type
  String name = "John Doe";
  print("Name: $name");

  // Boolean data type
  bool isStudent = true;
  print("Is Student: $isStudent");

  // List data type (List of integers)
  List<int> numbers = [1, 2, 3, 4, 5];
  print("Numbers: $numbers");

  // Map data type (Map of String keys and int values)
  Map<String, int> scores = {
    'Math': 90,
    'Science': 85,
    'English': 88,
  };
  print("Scores: $scores");
}